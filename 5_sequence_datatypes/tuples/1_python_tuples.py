"""
 A tuple is a collection data type that is similar to a list but is immutable, meaning its
 elements cannot be modified after the tuple is created. Tuples are defined by enclosing
 a sequence of elements in parentheses '()'.
 tuples can contain data of one or more datatypes including other tuples. Please refer the
 following example for tuple declaration and initialization.
"""
tuple1 = (1, 2, 3, "abc")
print(tuple1)

# we can also declare a tuple without parenthesis.
tuple2 = 1, 2, 3, "abc"
print(tuple2)
"""
Although its possible to declare and initialize tuples without the use of parenthesis, its
recommended to use parenthesis as in certain cases parenthesis are mandated.
"""